   public void populate() {
    vnum = Integer.parseInt(jTextField1.getText());
    maxnum = Float.parseFloat(jTextField3.getText());      //retrieves values
    minnum = Float.parseFloat(jTextField2.getText());
      float [] arr = new float [vnum];             //creates array of values
        Random rd = new Random();                  //declares random variable 
        for (int i = 0; i < vnum; i++) {           //loop to populate array
        arr[i] = rd.nextFloat() * (maxnum - minnum) + minnum;     //generates random values
   }
     raw = arr;
}

